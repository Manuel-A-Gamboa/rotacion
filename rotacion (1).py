import numpy as np,math


def rot_x(x, y, z, theta):
    '''
    rota un vector en el eje x utilizando sus coordenadas y un angulo

    Parameters:
        x (float): valor del vector en el eje x
        y (float): valor del vector en el eje y
        z (float): valor del vector en el eje z
        theta (float): valor del angulo en grados

    Returns:
        numpy.ndarray: el vector ya rotado

    '''
    rad = math.radians(theta)
    vector = np.array([x,y,z])
    matrizX = np.array([[1,0,0],[0,math.cos(rad),- math.sin(rad)],[0,math.sin(rad),math.cos(rad)]])
    return matrizX @ vector

